export const humanizeFilename = (path: string): string => {
  const filename = path.split('/').pop() || '';
  return filename
    .replace(/\.[^/.]+$/, "")       // Remove extension
    .replace(/[-_]/g, " ")          // Replace hyphens/underscores with spaces
    .replace(/\s+/g, " ")           // Collapse multiple spaces
    .replace(/\b\w/g, (c) => c.toUpperCase()) // Title-case each word
    .replace(/\s\(.*?\)$/, "")      // Remove trailing bracketed numbers like (2)
    .trim();
};

const createAsset = (
  id: string,
  label: string,
  section: MediaSection,
  src: string,
  path: string,
  notes?: string,
  mimeType?: string,
  tags?: string[],
): MediaAsset => {
  // If label is generic or empty, use the humanized filename
  const finalLabel = label.match(/^(Unit|fact|infra|machine|quality|team)-\d+$/) || !label
    ? humanizeFilename(path)
    : label;

  return { id, label: finalLabel, section, src, path, notes, mimeType, tags };
};

